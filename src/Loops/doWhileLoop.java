package Loops;

public class doWhileLoop {
    public static void main(String[] args) {
        int i =0;
        int j =0;
        do {
            System.out.println("First Print");
            i++;
        }while(i<10);

        // While Loop

        while(j<2){
            System.out.println("While Loop");
            j++;
        }
    }
}
